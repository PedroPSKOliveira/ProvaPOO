    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jLabel3.setText("");
        jLabel5.setText("");
        if (valor1.getText().isBlank()){
            jLabel3.setText("Deve inserir um valor");
        }
        else if(jRadioButton4.isSelected()  && !valor2.getText().isBlank()){
            Double valorC1 = Double.parseDouble(valor1.getText());
            Double valorC2 = Double.parseDouble(valor2.getText());
            jLabel2.setText("Potencia "+ pot(valorC1, valorC2));
        }
        else if(jRadioButton5.isSelected() && !valor2.getText().isBlank()){
            Double valorC1 = Double.parseDouble(valor1.getText());
            Double valorC2 = Double.parseDouble(valor2.getText());
            jLabel2.setText("Multiplicação "+ mult(valorC1, valorC2));
        }
        else if(jRadioButton1.isSelected()  && !valor2.getText().isBlank()){
            Double valorC1 = Double.parseDouble(valor1.getText());
            Double valorC2 = Double.parseDouble(valor2.getText());
            jLabel2.setText("Soma "+ somar(valorC1, valorC2));
       }
       else if (jRadioButton2.isSelected() && !valor2.getText().isBlank()){
            Double valorC1 = Double.parseDouble(valor1.getText());
            Double valorC2 = Double.parseDouble(valor2.getText());
            jLabel2.setText("Subtração "+ subtrair(valorC1, valorC2));
       }
       else if(jRadioButton3.isSelected() && !valor2.getText().isBlank()){
            Double valorC1 = Double.parseDouble(valor1.getText());
            Double valorC2 = Double.parseDouble(valor2.getText());
            jLabel2.setText("Divisão "+ div(valorC1, valorC2));
       }
       else if(valor2.getText().isBlank()){
            Double valorC1 = Double.parseDouble(valor1.getText());
            jLabel2.setText("Raiz "+ rad(valorC1));
        }
       else{
           jLabel2.setText("Deve inserir uma operação");
       }
       
    }//GEN-LAST:event_jButton1ActionPerformed

