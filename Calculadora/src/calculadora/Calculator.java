    @Override
    public String somar(double v1, double v2) {
        return String.valueOf(v1 + v2);

    }

    @Override
    public String subtrair(double v1, double v2) {
        return String.valueOf(v1 - v2);

    }

    @Override
    public String mult(double v1, double v2) {
        return String.valueOf(v1 * v2);
    }

    @Override
    public String pot(double v1, double v2) {
        return String.valueOf(Math.pow(v1, v2));

    }

    @Override
    public String rad(double v1) {
        return String.valueOf(Math.sqrt(v1));
    }
    
    @Override
    public String div(double v1, double v2){
        return String.valueOf(v1 / v2);
    }

